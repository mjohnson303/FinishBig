package finishbig.test.org;

public class SearchResults {
	private String name = "";
	private String time = "";
	private String length = "";
	private String date="";

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setTime(String cityState) {
		this.time = cityState;
	}

	public String getTime() {
		return time;
	}

	public void setLength(String phone) {
		this.length = phone;
	}

	public String getLength() {
		return length;
	}
	
	public void setDate(String phone) {
		this.date = phone;
	}

	public String getDate() {
		return date;
	}
}